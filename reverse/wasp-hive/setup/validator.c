char key[] = {0x8f,0xcc,0x9f,0xdc,0xa7,0xd0,0xe3,0x81,0xb5,0xc6,0xf3,0x96,0xfb,0x99,0xf5,0x8c,0xd3,0xe2,0x91,0xce,0xfe,0x8b,0xbc,0xe3,0xd3,0xb5,0xea,0x9e,0xf6,0xc7,0xb4,0xeb,0x9c,0xac,0xde,0xb2,0xd6,0xf7,0xd6,0xf7,0x8a};

int check(char *mykey) {
  char iv = 0xcc;
  for (int i = 0; mykey[i] != '\0'; i++) {
    iv ^= mykey[i];
    if (key[i] != iv) {
      return 0;
    }
  }
  return 1;
}